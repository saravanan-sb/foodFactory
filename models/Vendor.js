const mongoose = require('mongoose');


const vendorSchema = new mongoose.Schema({

    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    date: {
        type: Date,
        default: Date.now
    }
});

module.exports = Vendor = mongoose.model('Vendor', vendorSchema);